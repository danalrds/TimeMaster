                                                        <div id="faq-sub-3" class="panel-collapse collapse">
                                                            <div class="panel-body">
                                                                Aceasta tehnologie inovatoare are un mecanism bazat pe quartz, care nu foloseste o baterie. Miscarea incheieturii incarca un acumulator care agreneaza mecanismul. Odata acumulatorul incarcat, modelele isi vor stoca energia necesara pentru 72 de zile, fara a fi purtate. Cele pentru doamne isi stocheaza energie pentru doar 3-7 zile. Desigur, daca ceasul este purtat zilnic, acumulatorul este incarcat in mod continuu. Ceasul alerteaza in momentul in care ramane fara energie, prin miscarea secundarului din doua in doua secunde.
																Mecanismul Kinetic a aparut ca urmare a provocarii de a crea un ceas quartz care isi genereaza propria energie. Componentele de baza ale mecanismului, inovatii remarcabile de altfel, sunt urmatoarele: micro-generator de electricitate, amplificator de tensiune electrica, circuit integrat cu consum minim de energie si motor pas cu pas. Micro-generatorul de electricitate cuprinde rotorul, rotita de transmitere, micro-rotorul, bobina si bateria reincarcabila. 
																In cazul ceasurilor kinetice miscarea mainii este transformata in energie electrica care incarca acumulatorul ceasului.
