                                                        <div id="faq-sub" class="panel-collapse collapse">
                                                            <div class="panel-body">
                                                                Mecanismul mecanic vine in diferite forme si dimensiuni pentru a se potrivi celor mai variate carcase. 
                                                                Dimensiunea mecanismului se masoara in ligne (1 ligne = 2.2558291 mm) sau milimetri. Fiecare mecanism detine o codificare, un calibru. 
                                                                Ceasul cu mecanism mecanic este alimentat prin strangerea unui arc din otel, care la destindere actioneaza angrenajul, punand in miscare indicatoarele ceasului. Pentru siguranta, remontoarul nu trebuie rotit de mai mult de 10 ori, spre a nu se pune in pericol integritatea arcului. In limba engleza mecanismul mecanic este denumit mechanical / hand-winding movement.
                                                            </div>
